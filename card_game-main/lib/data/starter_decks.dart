  // Ottieni il mazzo base per una classe specifica
  static List<Card> getStarterDeck(String fighterClass) {
    switch (fighterClass.toLowerCase()) {
      case 'corrupt politician':
      case 'corruptpolitician':
        return corruptPoliticianDeck;
      case 'attack journalist':
      case 'attackjournalist':
        return attackJournalistDeck;
      case 'activist':
        return activistDeck;
      case 'bureaucrat':
        return bureaucratDeck;
      default:
        return corruptPoliticianDeck; // Default fallback
    }
  }
